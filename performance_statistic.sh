#!/bin/bash

cpu_info_lscpu=`lscpu`
cpu_info_dmidecode=`dmidecode -t processor`
memory_info=`free -m`
disk_info=`df -h`


cat <<!
=============================================================================
--------------------CPU_INFO(lscpu)------------------------------------------
$cpu_info_lscpu
-----------------------------------------------------------------------------
--------------------CPU_INFO(dmidecode)--------------------------------------
$cpu_info_dmidecode
-----------------------------------------------------------------------------
=============================================================================
--------------------MEMORY_INFO[MB]------------------------------------------
$memory_info
-----------------------------------------------------------------------------
=============================================================================
--------------------DISK_INFO------------------------------------------------
$disk_info
-----------------------------------------------------------------------------
=============================================================================

!
